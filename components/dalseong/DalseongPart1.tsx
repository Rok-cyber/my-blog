import React from 'react'

type FigureProps = {
  /** Image key/name (e.g., "d1_1") */
  name: string
  alt: string
  /** Optional caption shown under the image */
  caption?: React.ReactNode
  /**
   * Base path for images under /public.
   * Tailwind Nextjs Starter Blog 기본은 보통 public/static/images 를 쓰니,
   * 기본값을 /static/images 로 둡니다.
   */
  basePath?: string
  /** File extension without dot. Default: png */
  ext?: 'png' | 'jpg' | 'jpeg' | 'webp' | 'svg'
}

/**
 * Figure helper
 * - 기본: /static/images/{name}.png
 * - 프로젝트에서 /images 를 쓰면 basePath="/images" 로 바꿔서 재사용 가능
 */
function Figure({ name, alt, caption, basePath = '/static/images', ext = 'png' }: FigureProps) {
  const src = `${basePath}/${name}.${ext}`

  return (
    <figure className="my-6">
      {/* eslint-disable-next-line @next/next/no-img-element */}
      <img src={src} alt={alt} className="h-auto w-full rounded-lg" />
      {caption ? (
        <figcaption className="mt-2 text-sm text-gray-500 dark:text-gray-400">{caption}</figcaption>
      ) : null}
    </figure>
  )
}

